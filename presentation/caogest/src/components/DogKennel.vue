<template>
    <div>
        <div>
        <v-row>
            <v-col cols="12" sm="6" offset-sm="2">
                    <v-btn class="ma-3" x-large color = "brown lighten-4" @click="registarCao()">Registar Cão</v-btn>
                    <v-container fluid>
                        <v-item-group
                            v-model="selected"
                            multiple
                        >
                            <v-card flat height = "20"></v-card>
                            <v-card class = " mx-auto" height = "80" width="2000" flat color = "brown lighten-5" v-if="this.disponiveis.length === 0"> 
                                <v-card-title class = "display-1 text-center justify-center"> Não existem cães registados neste canil </v-card-title>
                            </v-card>
                            <v-row>
                                <v-col
                                    v-for="obj in disponiveis"
                                    :key="obj.nome"
                                    class="d-flex child-flex"
                                    cols="4"
                                >
                                
                                    <v-card flat tile>
                                        <v-item v-slot:default="{ active, toggle }">
                                            <v-img
                                                :src="require(`@/assets/${getPath(obj)}`)"
                                                aspect-ratio="1"
                                                class="grey lighten-2"
                                                @click="toggle"
                                            >
                                                <template v-slot:placeholder>
                                                    <v-row class="fill-height ma-0" align="center" justify="center">
                                                        <v-progress-circular indeterminate color="grey lighten-5"></v-progress-circular>
                                                    </v-row>
                                                </template>  
                                            </v-img>
                                        </v-item>
                                    <v-card-actions class="justify-center">
                                       <v-btn 
                                            text
                                            x-large
                                            class = "headline"
                                            @click="cao(obj)"
                                        > {{obj.nome}} 
                                        </v-btn>  
                                    </v-card-actions>
                                </v-card>
                            </v-col>
                        </v-row>
                    </v-item-group>
                </v-container>      
                </v-col>
                <v-spacer></v-spacer>

                <v-col cols = "3">
                    <v-row>
                        <v-card flat>
                            <v-card-text align = "start" justify= "end" >
                                <v-card-text class="display-2 font-weight-bold black--text">Pesquisar Cães</v-card-text>
                                <v-card flat height= "50" color = "white"></v-card>
                                    <v-text-field flat color = "grey" name="input-7-1" outlined label="Raça" ></v-text-field>
                                    <v-select 
                                        color = "grey"
                                        name="input-7-1"
                                        flat outlined 
                                        :items="itemscor"
                                        label="Cor"
                                    ></v-select>
                                    <v-select 
                                        color = "grey"
                                        name="input-7-1"
                                        flat outlined 
                                        :items="itemssexo"
                                        label="Género"
                                    ></v-select>
                                    <v-text-field flat color = "grey" name="input-7-1" outlined label="Idade" ></v-text-field>
                                    <v-select 
                                        color = "grey"
                                        name="input-7-1"
                                        flat outlined 
                                        :items="itemsporte"
                                        label="Porte"
                                    ></v-select>
                                    <v-text-field flat color = "grey" name="input-7-1" outlined label="Distrito" ></v-text-field>
                            </v-card-text>
                            <v-card-actions>
                                <v-spacer></v-spacer>
                                <v-btn class="ma-6" x-large color = "brown lighten-4" @click="dialog = false">Pesquisar</v-btn>
                            </v-card-actions>
                        </v-card>
                    </v-row>
                </v-col>
            </v-row>
        </div>
    </div>
</template>

<script>
import axios from 'axios'
import store from '@/store.js'
const lhost = require("@/config/global").host;


export default {
    name: "DogKennel",
    props:['id'],

    data: () => ({   
        fab:false,  
        caes: [],   
        disponivel: [],    
        selected: [], 
        itemssexo: [ 
            '- Selecionar -',
            'Macho',
            'Fêmea',
        ],
        itemsporte: [ 
            '- Selecionar -',
            'Pequeno',
            'Médio',
            'Grande',
        ], 
        itemscor: [
            '- Selecionar -',
            'Amarelo',
            'Azul',
            'Branco',
            'Castanho',
            'Cinzento',
            'Creme',
            'Dourado',
            'Fulvo',
            'Malhado',
            'Preto',
            'Vermelho',
        ],
        fotos: []  
    }),
    methods:{
        atualiza: async function(){
            try {
                let response = await axios.get(lhost + "/api/CaesEmailCanil/" + this.id,
                { headers: 
                    { "Authorization": 'Bearer ' + store.getters.token }
                });
                response.data.sort((a, b) => (a.nome > b.nome) ? 1 : -1);
                this.caes = response.data;
                this.ready = true;
            } 
            catch (e) {
               if(e.message == "Request failed with status code 401"){
                this.$store.commit("limpaStore");
                this.$router.push("/");
            }
            }
        },
        registarCao(){
           this.$router.push("/registar/cao/" + this.id); 
        },

        getPath: function(e) {
           return e.fotos[0].path
        },
        cao: function(obj){
            this.$router.push("/cao/canil/" + this.id + '/' + obj.idCao );
        },
    },
    created: async function(){
        try {
        let response = await axios.get(lhost + "/api/CaesEmailCanil/" + this.id,
            { headers: 
              { "Authorization": 'Bearer ' + store.getters.token }
            });
        response.data.sort((a, b) => (a.nome > b.nome) ? 1 : -1);
        this.caes = response.data;
        this.ready = true;
        } 
        catch (e) {
        if(e.message == "Request failed with status code 401"){
                this.$store.commit("limpaStore");
                this.$router.push("/");
            }
        }   
    },
    computed: {
        disponiveis: function () {
        return this.caes.filter(function (disponivel) {
            return (disponivel.estado !== "Apagado" && disponivel.estado !== "Adotado")
        })
        }, 
    }    

}
</script>
